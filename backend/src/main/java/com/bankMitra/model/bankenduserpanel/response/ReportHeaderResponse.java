package com.bankMitra.model.bankenduserpanel.response;

public class ReportHeaderResponse {

	private static final long serialVersionUID = 1L;
	
	private String e2eNumber;
	private String applicantName;
	private String cifNumber;
	private String loanDate;
	private String loanUnit;
	private String loanUser;
	private String loanScheme;
	private String loanVariant;
	public String getE2eNumber() {
		return e2eNumber;
	}
	public void setE2eNumber(String e2eNumber) {
		this.e2eNumber = e2eNumber;
	}
	public String getApplicantName() {
		return applicantName;
	}
	public void setApplicantName(String applicantName) {
		this.applicantName = applicantName;
	}
	public String getCifNumber() {
		return cifNumber;
	}
	public void setCifNumber(String cifNumber) {
		this.cifNumber = cifNumber;
	}
	public String getLoanDate() {
		return loanDate;
	}
	public void setLoanDate(String loanDate) {
		this.loanDate = loanDate;
	}
	public String getLoanUnit() {
		return loanUnit;
	}
	public void setLoanUnit(String loanUnit) {
		this.loanUnit = loanUnit;
	}
	public String getLoanUser() {
		return loanUser;
	}
	public void setLoanUser(String loanUser) {
		this.loanUser = loanUser;
	}
	public String getLoanScheme() {
		return loanScheme;
	}
	public void setLoanScheme(String loanScheme) {
		this.loanScheme = loanScheme;
	}
	public String getLoanVariant() {
		return loanVariant;
	}
	public void setLoanVariant(String loanVariant) {
		this.loanVariant = loanVariant;
	}
	public static long getSerialversionuid() {
		return serialVersionUID;
	}
	
	
}
